CREATE TABLE semester_field_subject (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    semester_id bigint NOT NULL,
    field_id bigint NOT NULL,
    subject_id bigint NOT NULL REFERENCES subject(id) ON DELETE CASCADE,
    CONSTRAINT uk_semester_field_subject UNIQUE (semester_id, field_id, subject_id),
    CONSTRAINT fk_curriculum_semester_field
        FOREIGN KEY (semester_id, field_id)
        REFERENCES semester_field (semester_id, field_id)
        ON DELETE CASCADE
        DEFERRABLE INITIALLY DEFERRED
);

