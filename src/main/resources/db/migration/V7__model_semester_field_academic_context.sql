CREATE OR REPLACE FUNCTION validate_class_meeting_semester_subject()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM student_group g
        JOIN semester_field_subject curriculum
          ON curriculum.semester_id = g.semester_id
         AND curriculum.field_id = g.field_id
        WHERE g.id = NEW.group_id
          AND curriculum.subject_id = NEW.subject_id
    ) THEN
        RAISE EXCEPTION
            'Subject % is not available for the selected group semester and field',
            NEW.subject_id;
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION validate_weekly_schedule_group_semester()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM student_group g
        WHERE g.id = NEW.group_id
          AND g.semester_id = NEW.semester_id
          AND g.field_id = NEW.field_id
    ) THEN
        RAISE EXCEPTION
            'Weekly schedule semester and field must match the selected group';
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION validate_weekly_schedule_semester_subject()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM semester_field_subject curriculum
        WHERE curriculum.semester_id = NEW.semester_id
          AND curriculum.field_id = NEW.field_id
          AND curriculum.subject_id = NEW.subject_id
    ) THEN
        RAISE EXCEPTION
            'Subject % is not available in semester % and field %',
            NEW.subject_id, NEW.semester_id, NEW.field_id;
    END IF;
    RETURN NEW;
END;
$$;

