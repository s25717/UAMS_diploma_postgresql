CREATE OR REPLACE FUNCTION validate_group_academic_context_update()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM subject_group sg
        WHERE sg.group_id = NEW.id
          AND NOT EXISTS (
              SELECT 1
              FROM semester_field_subject curriculum
              WHERE curriculum.semester_id = NEW.semester_id
                AND curriculum.field_id = NEW.field_id
                AND curriculum.subject_id = sg.subject_id
          )
    ) THEN
        RAISE EXCEPTION
            'Group % has subjects outside its new semester and field curriculum',
            NEW.id;
    END IF;

    IF EXISTS (
        SELECT 1
        FROM class_meeting cm
        JOIN semester sem ON sem.id = NEW.semester_id
        WHERE cm.group_id = NEW.id
          AND (
              sem.start_date IS NOT NULL AND cm.meeting_date < sem.start_date
              OR sem.end_date IS NOT NULL AND cm.meeting_date > sem.end_date
          )
    ) THEN
        RAISE EXCEPTION
            'Group % has class meetings outside its new semester period',
            NEW.id;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION protect_used_curriculum_assignment()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM subject_group sg
        JOIN student_group g ON g.id = sg.group_id
        WHERE g.semester_id = OLD.semester_id
          AND g.field_id = OLD.field_id
          AND sg.subject_id = OLD.subject_id
    ) OR EXISTS (
        SELECT 1
        FROM weekly_schedule_entry w
        WHERE w.semester_id = OLD.semester_id
          AND w.field_id = OLD.field_id
          AND w.subject_id = OLD.subject_id
    ) THEN
        RAISE EXCEPTION
            'Cannot remove curriculum subject % while it is used in semester % and field %',
            OLD.subject_id, OLD.semester_id, OLD.field_id;
    END IF;
    RETURN OLD;
END;
$$;

CREATE OR REPLACE FUNCTION validate_semester_has_field()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    checked_semester_id bigint;
BEGIN
    IF TG_TABLE_NAME = 'semester' THEN
        checked_semester_id := NEW.id;
    ELSIF TG_OP = 'DELETE' THEN
        checked_semester_id := OLD.semester_id;
    ELSE
        checked_semester_id := NEW.semester_id;
    END IF;

    IF EXISTS (SELECT 1 FROM semester WHERE id = checked_semester_id)
       AND NOT EXISTS (
           SELECT 1
           FROM semester_field
           WHERE semester_id = checked_semester_id
       ) THEN
        RAISE EXCEPTION
            'Semester % must belong to at least one field of study',
            checked_semester_id;
    END IF;

    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION validate_active_subject_has_teacher()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    subject_to_check bigint;
    teacher_count integer;
    active_count integer;
BEGIN
    IF TG_TABLE_NAME = 'teacher_subject'
       AND TG_OP = 'UPDATE'
       AND OLD.subject_id IS DISTINCT FROM NEW.subject_id THEN
        subject_to_check := OLD.subject_id;

        SELECT COUNT(*)
        INTO active_count
        FROM (
            SELECT subject_id
            FROM semester_field_subject
            WHERE subject_id = subject_to_check
            UNION ALL
            SELECT subject_id
            FROM subject_group
            WHERE subject_id = subject_to_check
        ) active_subject;

        IF active_count > 0 THEN
            SELECT COUNT(*)
            INTO teacher_count
            FROM teacher_subject
            WHERE subject_id = subject_to_check;

            IF teacher_count < 1 THEN
                RAISE EXCEPTION
                    'Active subject % must have at least one qualified teacher',
                    subject_to_check;
            END IF;
        END IF;
    END IF;

    IF TG_OP = 'DELETE' THEN
        subject_to_check := OLD.subject_id;
    ELSE
        subject_to_check := NEW.subject_id;
    END IF;

    SELECT COUNT(*)
    INTO active_count
    FROM (
        SELECT subject_id
        FROM semester_field_subject
        WHERE subject_id = subject_to_check
        UNION ALL
        SELECT subject_id
        FROM subject_group
        WHERE subject_id = subject_to_check
    ) active_subject;

    IF active_count > 0 THEN
        SELECT COUNT(*)
        INTO teacher_count
        FROM teacher_subject
        WHERE subject_id = subject_to_check;

        IF teacher_count < 1 THEN
            RAISE EXCEPTION
                'Active subject % must have at least one qualified teacher',
                subject_to_check;
        END IF;
    END IF;

    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    END IF;
    RETURN NEW;
END;
$$;

