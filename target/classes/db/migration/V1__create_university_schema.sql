CREATE EXTENSION IF NOT EXISTS btree_gist;

CREATE TABLE person (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name varchar(40) NOT NULL,
    surname varchar(60) NOT NULL,
    birth_date date NOT NULL,
    primary_email varchar(255),
    password_hash varchar(255) NOT NULL
);

CREATE TABLE person_emails (
    person_id bigint NOT NULL REFERENCES person(id) ON DELETE CASCADE,
    email varchar(255) NOT NULL,
    PRIMARY KEY (person_id, email),
    CONSTRAINT uk_person_emails_email UNIQUE (email)
);

CREATE TABLE student (
    id bigint PRIMARY KEY REFERENCES person(id) ON DELETE CASCADE,
    student_number varchar(255) NOT NULL,
    group_id bigint,
    CONSTRAINT uk_student_student_number UNIQUE (student_number),
    CONSTRAINT chk_student_number_format CHECK (student_number ~ '^s[0-9]{3,6}$')
);

CREATE TABLE teacher (
    id bigint PRIMARY KEY REFERENCES person(id) ON DELETE CASCADE,
    teacher_employee_number varchar(255) NOT NULL,
    CONSTRAINT uk_teacher_employee_number UNIQUE (teacher_employee_number)
);

CREATE TABLE administrator (
    id bigint PRIMARY KEY REFERENCES person(id) ON DELETE CASCADE,
    admin_employee_number varchar(255) NOT NULL,
    CONSTRAINT uk_administrator_employee_number UNIQUE (admin_employee_number)
);

CREATE TABLE field_of_study (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name varchar(255) NOT NULL,
    CONSTRAINT uk_field_of_study_name UNIQUE (name)
);

CREATE TABLE semester (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    number integer NOT NULL,
    start_date date,
    end_date date,
    field_id bigint NOT NULL REFERENCES field_of_study(id),
    CONSTRAINT chk_semester_number CHECK (number BETWEEN 1 AND 8),
    CONSTRAINT chk_semester_dates CHECK (start_date IS NULL OR end_date IS NULL OR start_date <= end_date)
);

CREATE TABLE student_group (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code varchar(255) NOT NULL,
    semester_id bigint NOT NULL REFERENCES semester(id),
    CONSTRAINT uk_student_group_code UNIQUE (code)
);

ALTER TABLE student
    ADD CONSTRAINT fk_student_group FOREIGN KEY (group_id) REFERENCES student_group(id);

CREATE TABLE subject (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name varchar(255) NOT NULL,
    CONSTRAINT uk_subject_name UNIQUE (name)
);

CREATE TABLE subject_group (
    subject_id bigint NOT NULL REFERENCES subject(id) ON DELETE CASCADE,
    group_id bigint NOT NULL REFERENCES student_group(id) ON DELETE CASCADE,
    PRIMARY KEY (subject_id, group_id)
);

CREATE TABLE teacher_subject (
    teacher_id bigint NOT NULL REFERENCES teacher(id) ON DELETE CASCADE,
    subject_id bigint NOT NULL REFERENCES subject(id) ON DELETE CASCADE,
    PRIMARY KEY (teacher_id, subject_id)
);

CREATE TABLE room (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    room_number varchar(255) NOT NULL,
    capacity integer NOT NULL,
    CONSTRAINT uk_room_number UNIQUE (room_number),
    CONSTRAINT chk_room_capacity CHECK (capacity >= 1)
);

CREATE TABLE weekly_schedule_entry (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id bigint NOT NULL REFERENCES student_group(id),
    subject_id bigint NOT NULL REFERENCES subject(id),
    teacher_id bigint NOT NULL REFERENCES teacher(id),
    class_type varchar(255) NOT NULL,
    meeting_mode varchar(255) NOT NULL,
    day_of_week varchar(255) NOT NULL,
    start_time time NOT NULL,
    end_time time NOT NULL,
    room_id bigint REFERENCES room(id),
    online_meeting_link varchar(255),
    semester_id bigint NOT NULL REFERENCES semester(id),
    CONSTRAINT chk_weekly_class_type CHECK (class_type IN ('LECTURE', 'TUTORIAL', 'LABORATORY')),
    CONSTRAINT chk_weekly_meeting_mode CHECK (meeting_mode IN ('ONLINE', 'CLASSROOM')),
    CONSTRAINT chk_weekly_day_of_week CHECK (day_of_week IN ('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY', 'SUNDAY')),
    CONSTRAINT chk_weekly_time_range CHECK (start_time < end_time),
    CONSTRAINT chk_weekly_location CHECK (
        (meeting_mode = 'ONLINE' AND online_meeting_link IS NOT NULL)
        OR (meeting_mode = 'CLASSROOM' AND room_id IS NOT NULL)
    )
);

CREATE TABLE class_meeting (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    room varchar(255),
    online_meeting_link varchar(255),
    location varchar(255),
    meeting_date date NOT NULL,
    day_of_week varchar(255) NOT NULL,
    start_time time NOT NULL,
    end_time time NOT NULL,
    class_type varchar(255) NOT NULL,
    meeting_mode varchar(255) NOT NULL,
    status varchar(255) NOT NULL,
    comment varchar(1000),
    subject_id bigint NOT NULL REFERENCES subject(id),
    teacher_id bigint NOT NULL REFERENCES teacher(id),
    group_id bigint NOT NULL REFERENCES student_group(id),
    schedule_entry_id bigint REFERENCES weekly_schedule_entry(id),
    CONSTRAINT chk_class_meeting_class_type CHECK (class_type IN ('LECTURE', 'TUTORIAL', 'LABORATORY')),
    CONSTRAINT chk_class_meeting_meeting_mode CHECK (meeting_mode IN ('ONLINE', 'CLASSROOM')),
    CONSTRAINT chk_class_meeting_status CHECK (status IN ('DRAFT', 'SCHEDULED', 'CANCELLED', 'COMPLETED')),
    CONSTRAINT chk_class_meeting_day_of_week CHECK (day_of_week IN ('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY', 'SUNDAY')),
    CONSTRAINT chk_class_meeting_time_range CHECK (start_time < end_time),
    CONSTRAINT chk_class_meeting_location CHECK (
        meeting_mode = 'ONLINE'
        OR location IS NOT NULL
        OR room IS NOT NULL
    )
);

CREATE TABLE attendance (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status varchar(255) NOT NULL,
    student_id bigint NOT NULL REFERENCES student(id),
    class_meeting_id bigint NOT NULL REFERENCES class_meeting(id) ON DELETE CASCADE,
    comment varchar(1000),
    CONSTRAINT uk_attendance_student_meeting UNIQUE (student_id, class_meeting_id),
    CONSTRAINT chk_attendance_status CHECK (status IN ('PRESENT', 'LATE', 'ABSENT', 'EXCUSED'))
);

CREATE TABLE attendance_report (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    generated_on date NOT NULL,
    report_type varchar(255) NOT NULL,
    date_from date,
    date_to date,
    class_type varchar(255),
    teacher_id bigint REFERENCES teacher(id),
    subject_id bigint REFERENCES subject(id),
    group_id bigint REFERENCES student_group(id),
    overall_performance_percentage double precision NOT NULL DEFAULT 0,
    CONSTRAINT chk_attendance_report_type CHECK (report_type IN ('SEMESTER', 'TEACHER', 'SUBJECT', 'GROUP', 'COMBINED')),
    CONSTRAINT chk_attendance_report_class_type CHECK (class_type IS NULL OR class_type IN ('LECTURE', 'TUTORIAL', 'LABORATORY')),
    CONSTRAINT chk_attendance_report_dates CHECK (date_from IS NULL OR date_to IS NULL OR date_from <= date_to),
    CONSTRAINT chk_attendance_report_percentage CHECK (overall_performance_percentage BETWEEN 0 AND 100)
);

CREATE TABLE attendance_report_semesters (
    report_id bigint NOT NULL REFERENCES attendance_report(id) ON DELETE CASCADE,
    semester_id bigint NOT NULL REFERENCES semester(id),
    PRIMARY KEY (report_id, semester_id)
);

CREATE TABLE report_line (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    total_meetings integer NOT NULL,
    present_count integer NOT NULL,
    absent_count integer NOT NULL,
    late_count integer NOT NULL,
    excused_count integer NOT NULL,
    attendance_percentage double precision NOT NULL,
    student_id bigint NOT NULL REFERENCES student(id),
    report_id bigint NOT NULL REFERENCES attendance_report(id) ON DELETE CASCADE,
    CONSTRAINT chk_report_line_counts CHECK (
        total_meetings >= 0
        AND present_count >= 0
        AND absent_count >= 0
        AND late_count >= 0
        AND excused_count >= 0
    ),
    CONSTRAINT chk_report_line_percentage CHECK (attendance_percentage BETWEEN 0 AND 100)
);

CREATE TABLE room_booking (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date date NOT NULL,
    start_time time NOT NULL,
    end_time time NOT NULL,
    booking_status varchar(255) NOT NULL,
    room_id bigint NOT NULL REFERENCES room(id) ON DELETE CASCADE,
    class_meeting_id bigint UNIQUE REFERENCES class_meeting(id),
    CONSTRAINT uk_room_booking_exact_slot UNIQUE (room_id, date, start_time, end_time),
    CONSTRAINT chk_room_booking_status CHECK (booking_status IN ('RESERVED', 'CONFIRMED', 'CANCELLED')),
    CONSTRAINT chk_room_booking_time_range CHECK (start_time < end_time),
    CONSTRAINT ex_room_booking_no_overlap EXCLUDE USING gist (
        room_id WITH =,
        tsrange((date + start_time), (date + end_time), '[)') WITH &&
    ) WHERE (booking_status <> 'CANCELLED')
);

CREATE TABLE notification (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    notification_type varchar(31) NOT NULL,
    message varchar(255) NOT NULL,
    title varchar(255),
    priority integer NOT NULL,
    status varchar(255) NOT NULL,
    recipient_id bigint REFERENCES person(id),
    has_attachment boolean,
    display_seconds integer,
    CONSTRAINT chk_notification_type CHECK (notification_type IN ('EMAIL', 'SYSTEM')),
    CONSTRAINT chk_notification_status CHECK (status IN ('DRAFT', 'PENDING', 'SENT', 'FAILED', 'CANCELLED')),
    CONSTRAINT chk_notification_priority CHECK (priority >= 1),
    CONSTRAINT chk_system_notification_display CHECK (
        notification_type <> 'SYSTEM'
        OR display_seconds IS NOT NULL AND display_seconds >= 1
    )
);

CREATE TABLE scheduled_notification_task (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_type varchar(255) NOT NULL,
    status varchar(255) NOT NULL,
    scheduled_at timestamp NOT NULL,
    processed_at timestamp,
    retry_count integer NOT NULL DEFAULT 0,
    failure_reason varchar(255),
    recipient_id bigint REFERENCES person(id),
    class_meeting_id bigint REFERENCES class_meeting(id),
    attendance_report_id bigint REFERENCES attendance_report(id),
    CONSTRAINT chk_notification_task_type CHECK (
        task_type IN (
            'CLASS_MEETING_REMINDER',
            'LOW_ATTENDANCE_WARNING',
            'ATTENDANCE_REGISTERED',
            'ATTENDANCE_UPDATED',
            'REPORT_READY'
        )
    ),
    CONSTRAINT chk_notification_task_status CHECK (status IN ('PENDING', 'PROCESSING', 'SENT', 'FAILED', 'CANCELLED')),
    CONSTRAINT chk_notification_task_retry_count CHECK (retry_count >= 0)
);

