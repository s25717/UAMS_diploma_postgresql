CREATE OR REPLACE FUNCTION validate_teacher_subject_assignment()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM teacher_subject ts
        WHERE ts.teacher_id = NEW.teacher_id
          AND ts.subject_id = NEW.subject_id
    ) THEN
        RAISE EXCEPTION 'Teacher % is not qualified for subject %', NEW.teacher_id, NEW.subject_id;
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION validate_subject_group_assignment()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM subject_group sg
        WHERE sg.subject_id = NEW.subject_id
          AND sg.group_id = NEW.group_id
    ) THEN
        RAISE EXCEPTION 'Subject % is not assigned to group %', NEW.subject_id, NEW.group_id;
    END IF;
    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION validate_attendance_student_group()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM student s
        JOIN class_meeting cm ON cm.id = NEW.class_meeting_id
        WHERE s.id = NEW.student_id
          AND s.group_id = cm.group_id
    ) THEN
        RAISE EXCEPTION 'Student % does not belong to the class meeting group', NEW.student_id;
    END IF;
    RETURN NEW;
END;
$$;

CREATE CONSTRAINT TRIGGER trg_class_meeting_teacher_subject
AFTER INSERT OR UPDATE OF teacher_id, subject_id ON class_meeting
DEFERRABLE INITIALLY DEFERRED
FOR EACH ROW
EXECUTE FUNCTION validate_teacher_subject_assignment();

